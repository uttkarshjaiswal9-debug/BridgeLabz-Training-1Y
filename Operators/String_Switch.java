public class String_Switch{
	public static void main (String[] args){
		char c = 'a' ;
		switch (c){
			case 'a' :
				System.out.println("Condition matched and print a");
				break ;
			case 'b' :
				System.out.println("Condition matched and print b");
				break ; 
			case 'c' :
				System.out.println("Condition matched and print c");
				break ; 
		}
	}
}